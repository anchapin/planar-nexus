/**
 * Regenerate video-derived fixture tests using `validateBehavior`.
 *
 * Reads each `src/lib/game-state/__tests__/video-derived/*.test.ts`,
 * extracts the `const <id> = { ... };` fixture literal, and rewrites the
 * file so that every `validates behavior: ...` it-block is replaced by a
 * call to `validateBehavior(behavior, fixture.gameState)`. The four
 * structural smoke tests are kept but rewritten to use real assertions.
 *
 * Run: `node scripts/regen-video-fixtures.mjs`
 *
 * Idempotent: re-running produces identical output (modulo whitespace).
 */

import * as fs from "fs";
import * as path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const FIXTURE_DIR = path.resolve(
  __dirname,
  "../src/lib/game-state/__tests__/video-derived",
);

function listFixtureFiles() {
  return fs
    .readdirSync(FIXTURE_DIR)
    .filter(
      (f) =>
        f.endsWith(".test.ts") &&
        !f.startsWith("_") &&
        !f.startsWith("__"),
    );
}

/**
 * Extract the `const <id> = { ... };` literal from the file body. We locate
 * the first occurrence of `const <id> = {` and walk braces to find the
 * matching `};`. This is intentionally tolerant: the fixture bodies are
 * auto-generated and uniform across all 50 files.
 */
function extractFixture(source, fixtureId) {
  const start = source.indexOf(`const ${fixtureId} = {`);
  if (start < 0) {
    throw new Error(`Could not find fixture literal: const ${fixtureId} = {`);
  }
  // Find matching closing `};` by counting braces (string-aware enough for
  // the auto-generated fixture bodies, which never contain `};` inside
  // string literals).
  let depth = 0;
  let end = -1;
  for (let i = start + `const ${fixtureId} = `.length; i < source.length; i++) {
    const ch = source[i];
    if (ch === "{") depth++;
    else if (ch === "}") {
      depth--;
      if (depth === 0) {
        // The closing `}` is at position i; the next char is the trailing
        // `;`. We want to include up to and including the `;`.
        end = i + 2;
        break;
      }
    }
  }
  if (end < 0) {
    throw new Error(`Could not find end of fixture literal for ${fixtureId}`);
  }
  return source.slice(start, end);
}

/**
 * Pull the description from the JSDoc header, if present.
 */
function extractDescription(source) {
  const match = source.match(/\* Description:\s*([^\n]+)/);
  return match ? match[1].trim() : "";
}

function regenerateFile(filename) {
  const filepath = path.join(FIXTURE_DIR, filename);
  const original = fs.readFileSync(filepath, "utf8");

  // The fixture id matches the filename (e.g. full-game-late-state.test.ts
  // → const full_game_late_state = { ... })
  const baseId = filename.replace(/\.test\.ts$/, "");
  // Hyphens → underscores (kebab → snake, matching the source pattern).
  const fixtureId = baseId.replace(/-/g, "_");
  const description = extractDescription(original);

  const fixtureLiteral = extractFixture(original, fixtureId);

  const titleHeader = `Video-Derived Fixture: ${baseId}`;
  const sourceLine = description ? ` * Description: ${description}\n` : "";
  const fixtureIdLine = ` * Fixture ID: ${baseId}\n * Source: video-derived (issue #1397 — see docs/TEST_VIDEO_FIXTURES.md)\n`;

  const output = [
    `/**\n`,
    ` * ${titleHeader}\n`,
    sourceLine,
    fixtureIdLine,
    ` *\n`,
    ` * Auto-generated by scripts/regen-video-fixtures.mjs (issue #1397).\n`,
    ` * Each "validates behavior" it-block now delegates to validateBehavior()\n`,
    ` * for a typed assertion against the RecognizedBoardState.\n`,
    ` */\n`,
    `\n`,
    `import { validateBehavior } from "./_helpers";\n`,
    `\n`,
    fixtureLiteral,
    `\n`,
    `\n`,
    `describe("${titleHeader}", () => {\n`,
    `  it("loads game state successfully", () => {\n`,
    `    expect(typeof ${fixtureId}.gameState).toBe("object");\n`,
    `    expect(${fixtureId}.gameState).not.toBeNull();\n`,
    `  });\n`,
    `\n`,
    `  it("has valid player data", () => {\n`,
    `    expect(Number.isInteger(${fixtureId}.gameState.player_life)).toBe(true);\n`,
    `    expect(Number.isInteger(${fixtureId}.gameState.opponent_life)).toBe(true);\n`,
    `    expect(Array.isArray(${fixtureId}.gameState.battlefield_player)).toBe(true);\n`,
    `    expect(Array.isArray(${fixtureId}.gameState.battlefield_opponent)).toBe(true);\n`,
    `  });\n`,
    `\n`,
    `  it("has valid turn structure", () => {\n`,
    `    expect(typeof ${fixtureId}.gameState.turn_number).toBe("number");\n`,
    `    expect(Number.isInteger(${fixtureId}.gameState.turn_number)).toBe(true);\n`,
    `    expect(${fixtureId}.gameState.turn_number).toBeGreaterThan(0);\n`,
    `    expect(typeof ${fixtureId}.gameState.phase).toBe("string");\n`,
    `    expect(${fixtureId}.gameState.phase.length).toBeGreaterThan(0);\n`,
    `  });\n`,
    `\n`,
    `  it("can be serialized and deserialized", () => {\n`,
    `    const serialized = JSON.stringify(${fixtureId}.gameState);\n`,
    `    const deserialized = JSON.parse(serialized);\n`,
    `    expect(deserialized).toEqual(${fixtureId}.gameState);\n`,
    `  });\n`,
    `\n`,
    `  for (const behavior of ${fixtureId}.expectedBehaviors) {\n`,
    `    it(\`validates behavior: \${behavior}\`, () => {\n`,
    `      // Real assertion: see _helpers/validate-fixture.ts for the\n`,
    `      // behaviour-string → typed-property mapping. Issue #1397 replaces\n`,
    `      // the previous TODO placeholder with a concrete property check.\n`,
    `      validateBehavior(behavior, ${fixtureId}.gameState);\n`,
    `    });\n`,
    `  }\n`,
    `});\n`,
  ].join("");

  fs.writeFileSync(filepath, output, "utf8");
}

function main() {
  const files = listFixtureFiles();
  process.stdout.write(`Regenerating ${files.length} video-derived fixture tests...\n`);
  for (const f of files) {
    regenerateFile(f);
    process.stdout.write(`  ✓ ${f}\n`);
  }
  process.stdout.write("Done.\n");
}

main();